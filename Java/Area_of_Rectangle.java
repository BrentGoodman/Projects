import java.util.Scanner;

class Solution {
	
	public static void main(String args[]) {
		
		Scanner sc = new Scanner(System.in);
		int length = sc.nextInt();
		int breadth = sc.nextInt();

		System.out.println(length * breadth);
		
	}
}

/*
Sample Input 1:
10
20

Sample Output 1:
200

Sample Input 2:
10
10

Sample Output 2:
100

*/